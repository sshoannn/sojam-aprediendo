    private void btnLoginUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginUserActionPerformed
        Cusuarios objCusuarios = new Cusuarios();
        
        if(!txtUserLogin.getText().equals("") && !txtPassLogin.getText().equals("")){
        
        
            objCusuarios.LoginUsuarios(txtUserLogin, txtPassLogin);
            FormAlumnos objFormAlumnos = new FormAlumnos();
            objFormAlumnos.setVisible(true);
            objFormAlumnos.setLocationRelativeTo(null);
            objFormAlumnos.setResizable(false);
            this.dispose();
            
            
            
        }else{
            JOptionPane.showMessageDialog(null, "Debes Ingresar tus datos");
        }
        
    }//GEN-LAST:event_btnLoginUserActionPerformed


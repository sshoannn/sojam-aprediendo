    private void btnRegistrarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarUsuarioActionPerformed
        Cusuarios objCusuarios = new Cusuarios();

        if(txtRegUsuario.getText().equals("") || txtRegPassUsuario.getText().equals("") || txtConfirmarPassUsuario.getText().equals("") || txtRegCorreoUsuario.getText().equals("")){

            JOptionPane.showMessageDialog(null, "Debes llenar el formulario");
        }else{

            String pass = txtRegPassUsuario.getText();
            String Conpass = txtConfirmarPassUsuario.getText();

            if(pass.equals(Conpass)){
                objCusuarios.registrarUsuarios(txtRegUsuario, txtRegPassUsuario, txtRegistroNombre, txtRegCorreoUsuario);

                LoginUsuario objUsuario = new LoginUsuario();
                objUsuario.setVisible(true);
                objUsuario.setLocationRelativeTo(null);
                objUsuario.setResizable(false);
                this.dispose();
            }else{

                JOptionPane.showMessageDialog(null, "Las contraseñas deben coincidir");

            }

        }

    }//GEN-LAST:event_btnRegistrarUsuarioActionPerformed

